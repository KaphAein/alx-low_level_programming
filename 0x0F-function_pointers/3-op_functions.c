#include "3-calc.h"

/**
 * op_add - function that takes a pointer
 * @a: element
 * @b: element
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that takes a pointer
 * @a: element
 * @b: element
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that takes a pointer
 * @a: element
 * @b: element
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that takes a pointer
 * @a: element
 * @b: element
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that takes a pointer
 * @a: element
 * @b: element
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
